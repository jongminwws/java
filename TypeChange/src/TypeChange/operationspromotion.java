package TypeChange;

public class operationspromotion {
    public static void main(String[] args) {
        byte x1 = 10;
        byte x2 = 20;
        int va = x1 + x2;
        System.out.println(va);
        char c1 = 65;
        char c2 = 1;
        int ca = c1 + c2;
        System.out.println("유니코드" + ca);
        System.out.println("출력문자" + (char)ca);
        int va3 = 10;
        int va4 = va3 / 4;
        System.out.println(va4);
        int v5 = 10;
        double v6 = (double)v5 / 4.0;
        System.out.println(v6);
        int x = 1;
        int y = 2; //
        double r = (double)x / (double)y;
        System.out.println(r);
    }
}
